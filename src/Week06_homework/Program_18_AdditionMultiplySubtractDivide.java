package Week06_homework;

import java.util.Scanner;

/**
 * 18. Write a Java program to print the sum (addition), multiply, subtract, divide and
 * remainder of two numbers.
 * Test Data:
 * Input first number: 125
 * Input second number: 24
 * Expected Output :
 * 125 + 24 = 149
 * 125 - 24 = 101
 * 125 x 24 = 3000
 * 125 / 24 = 5
 * 125 mod 24 = 5
 */
public class Program_18_AdditionMultiplySubtractDivide {
    public static void main(String[] args) {
        Scanner rashang = new Scanner(System.in);
        System.out.println(" Input number First Number  =  ");
        int a = rashang.nextInt();
        System.out.println(" Input number Second number  =  ");
        int b = rashang.nextInt();
        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + (a / b));
        System.out.println(a + " mod " + b + " = " + (a % b));
        rashang.close();
    }
}
